/// IconSizesDS - Tokens para tamaños de íconos.
/// Mantiene la consistencia visual de la iconografía.
class IconSizesDS {
  IconSizesDS._();

  /// 14px - Para íconos pequeños, como en botones `sm`.
  static const double sm = 14.0;

  /// 16px - Tamaño estándar para la mayoría de los íconos.
  static const double md = 16.0;

  /// 20px - Para íconos más grandes y prominentes.
  static const double lg = 20.0;

  /// 24px - Tamaño común para íconos en barras de navegación o titulares.
  static const double xl = 24.0;
}
